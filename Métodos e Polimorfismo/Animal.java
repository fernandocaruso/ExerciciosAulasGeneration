package Exercicios;

public class Animal {
	
	String nome;
	String idade;
	String qualSom;
	String velocidade;
	
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getIdade() {
		return idade;
	}
	public void setIdade(String idade) {
		this.idade = idade;
	}
	public String getQualSom() {
		return qualSom;
	}
	public void setQualSom(String qualSom) {
		this.qualSom = qualSom;
	}
	public String getVelocidade() {
		return velocidade;
	}
	public void setVelocidade(String velocidade) {
		this.velocidade = velocidade;
	}

}

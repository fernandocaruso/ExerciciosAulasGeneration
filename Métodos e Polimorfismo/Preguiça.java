package Exercicios;

public class Preguiça extends Animal {
	
	String horasDeSono;
	String alturaMaxima;
	
	public String getHorasDeSono() {
		return horasDeSono;
	}
	public void setHorasDeSono(String horasDeSono) {
		this.horasDeSono = horasDeSono;
	}
	public String getAlturaMaxima() {
		return alturaMaxima;
	}
	public void setAlturaMaxima(String alturaMaxima) {
		this.alturaMaxima = alturaMaxima;
	}

}
